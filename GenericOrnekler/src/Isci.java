public class Isci extends Calisanlar{

    private boolean eyt;
    private boolean cocukYardimi;
    private Double sodexo;

    public boolean isEyt() {
        return eyt;
    }

    public void setEyt(boolean eyt) {
        this.eyt = eyt;
    }

    public boolean isCocukYardimi() {
        return cocukYardimi;
    }

    public void setCocukYardimi(boolean cocukYardimi) {
        this.cocukYardimi = cocukYardimi;
    }

    public Double getSodexo() {
        return sodexo;
    }

    public void setSodexo(Double sodexo) {
        this.sodexo = sodexo;
    }
}
